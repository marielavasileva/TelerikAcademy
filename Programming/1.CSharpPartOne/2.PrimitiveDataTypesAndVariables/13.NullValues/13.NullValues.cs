using System;



    class NullValues
    {
        static void Main()
        {
            int value1 = 0;
            double value2 = 0d;
            int? value11 = null;
            double? value21 = null;
            int value12 = 5;
            double value22 = 2.5d;
            Console.WriteLine(value1);
            Console.WriteLine(value2);
            Console.WriteLine(value11);
            Console.WriteLine(value21);
            Console.WriteLine(value12);
            Console.WriteLine(value22);
        }
    }

